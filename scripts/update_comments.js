async function main() {
  const auth = new google.auth.GoogleAuth({
    credentials: {
      client_email: GOOGLE_SERVICE_ACCOUNT_EMAIL,
      private_key: GOOGLE_PRIVATE_KEY,
    },
    scopes: ['https://www.googleapis.com/auth/spreadsheets'],
  });

  const sheets = google.sheets({ version: 'v4', auth });

  // 기존 데이터 읽기
  const response = await sheets.spreadsheets.values.get({
    spreadsheetId: SPREADSHEET_ID,
    range: '내신기출성적!A:O',
  });

  const rows = response.data.values;
  const headers = rows[0];

  // 총평 컬럼 인덱스 찾기
  let commentIndex = headers.indexOf('총평');
  if (commentIndex === -1) {
    // 총평 컬럼이 없으면 추가
    console.log('총평 컬럼이 없어서 추가합니다...');
    commentIndex = headers.length;
    await sheets.spreadsheets.values.update({
      spreadsheetId: SPREADSHEET_ID,
      range: `내신기출성적!${String.fromCharCode(65 + commentIndex)}1`,
      valueInputOption: 'RAW',
      requestBody: {
        values: [['총평']]
      }
    });
    console.log('총평 컬럼 추가 완료 (열:', String.fromCharCode(65 + commentIndex) + ')');
  }

  // 이름 컬럼 인덱스
  const nameIndex = headers.indexOf('이름');

  console.log('총평 업데이트 시작...');
  console.log('총 행 수:', rows.length);

  // 각 행의 총평 업데이트
  const updates = [];
  for (let i = 1; i < rows.length; i++) {
    const row = rows[i];
    const name = row[nameIndex];

    if (COMMENTS[name]) {
      updates.push({
        range: `내신기출성적!${String.fromCharCode(65 + commentIndex)}${i + 1}`,
        values: [[COMMENTS[name]]]
      });
    }
  }

  if (updates.length > 0) {
    await sheets.spreadsheets.values.batchUpdate({
      spreadsheetId: SPREADSHEET_ID,
      requestBody: {
        valueInputOption: 'RAW',
        data: updates
      }
    });
    console.log(`✅ ${updates.length}명의 총평 업데이트 완료!`);
  } else {
    console.log('업데이트할 데이터가 없습니다.');
  }
}

main().catch(console.error);
